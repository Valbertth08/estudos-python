
#substring
nome="pedro augusto"
print(nome[0])#só a primeira letra
print(nome[:8])# como não informei o inicio, ele começa por padrão 0 e vai até o 8
print(nome[5:])#começa do 5 vai até o final da string
print(nome[3:6])# pegando uma string de um de um detrminado tamanho
print(nome[3:6:2])#pegando uma string começando do 3 vai até o 6 pulando de 2 em 2
print(nome[:])# me retorna a string inteira de volta
print(nome[::-1])# cria um string invertida